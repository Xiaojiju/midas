package com.mtfm.backend_mall.web;

import java.io.Serializable;
/**
 * @author 一块小饼干
 * @since 1.0.0
 * 创建对象的返回id实体
 */
public class IdTemplate implements Serializable {

    private Long id;

    public IdTemplate() {
    }

    public IdTemplate(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
}
